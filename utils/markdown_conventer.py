import re


def convert_headings_to_bold(text):
    """
    Преобразует заголовки Markdown (###, ##, #) в жирный текст для Telegram
    """
    # Заменяем заголовки ### Заголовок на **Заголовок**
    text = re.sub(r'###\s+(.+?)(?=\n|$)', r'*\1*', text)

    # Заменяем заголовки ## Заголовок на **Заголовок**
    text = re.sub(r'##\s+(.+?)(?=\n|$)', r'*\1*', text)

    # Заменяем заголовки # Заголовок на **Заголовок**
    text = re.sub(r'#\s+(.+?)(?=\n|$)', r'*\1*', text)

    return text
